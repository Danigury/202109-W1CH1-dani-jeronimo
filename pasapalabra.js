//<<<<<< RANDOM QUESTIONS
function randomQuestions() {
    randomQuest = Math.floor(Math.random() * mixQuest.length);
    questSelect = mixQuest[randomQuest];
    return questSelect;
}

//<<<<<< DELETING QUESTIONS
function deleteQuestions() {
    for (let i = 0; i < mixQuest.length; i++) {
        if (questSelect === mixQuest[i]) {
            mixQuest.splice(randomQuest, 1);
        }
    }
}

//<<<<<< VALIDANDO RESPUESTAS
document.addEventListener('DOMContentLoaded', function () {
    document.querySelector('.formulario').addEventListener('submit', validarRespuesta)
});

function validarRespuesta(e) {
    e.preventDefault();
    let respuesta = document.querySelector('.respuesta').value;
    respuesta = noAcento(respuesta.toLowerCase());
    if (respuesta === questSelect[0].answer) {
        letraAcertada();
        questSelect.splice(0, 1);
        letrasRosco.splice(0, 1);
        contadorAciertos += 1;
        contadorRestantes -= 1;
        document.querySelector('#respuestas-acertadas').innerHTML = contadorAciertos;
        document.querySelector('#palabras-restantes').innerHTML = contadorRestantes;
        document.querySelector('.formulario').reset();
        mostrarDefinicion();

    } else {
        letraErronea();
        contadorRestantes -= 1;
        questSelect.splice(0, 1);
        letrasRosco.splice(0, 1);
        document.querySelector('#palabras-restantes').innerHTML = contadorRestantes;
        document.querySelector('.formulario').reset();
        mostrarDefinicion();
    }

}
botonResponder.addEventListener('click', validarRespuesta);

